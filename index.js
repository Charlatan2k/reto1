function suma(op1, op2, op3){
  return op1 + op2 + op3;
  
}

function subs(op1, op2){
  return op1 - op2
}

function mult(op1, op2){
  return op1 * op2
}

function div(op1, op2){
  return op1 / op2
}

function cuadrado(op1){
  let result = op1^2;
  return result;
}


